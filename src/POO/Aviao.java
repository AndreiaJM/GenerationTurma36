package POO;

public class Aviao {
	
	private int totalAcentos;
	private float valorPassagem;
	private float valorPetPassagem;
	
	public int getTotalAcentos()
	{
		return totalAcentos;
	}
	
	public void setTotalAcentos(int totalAcentos)
	{
		this.totalAcentos=totalAcentos;
	}
	
	public float getValorPassagem()
	{
		return valorPassagem;
	}
	public void setValorPassagem(float valorPassagem)
	{
		this.valorPassagem=valorPassagem;
	}
	public float getValorPetPassagem()
	{
		return valorPetPassagem;
	}
	public void setValorPetPassagem(float valorPetPassagem)
	{
		this.valorPetPassagem=valorPetPassagem;
	}

}
